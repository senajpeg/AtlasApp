// lib/features/expert/models/expert_model.dart

class ExpertModel {
  final String id;
  final String name;
  final String title;
  final String experience;
  final String rating;
  final String phoneNumber;
  final String category;

  ExpertModel({
    required this.id,
    required this.name,
    required this.title,
    required this.experience,
    required this.rating,
    required this.phoneNumber,
    required this.category,
  });
}